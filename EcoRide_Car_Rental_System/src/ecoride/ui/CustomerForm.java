    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    String id = lblCustomerId.getText();
    String name = txtName.getText();
    String nic = txtNic.getText();
    String phone = txtPhone.getText();
    String email = txtEmail.getText();
    String address = txtAddress.getText();

    Customer c = new Customer(id, name, nic, phone, email, address);
    EcoRideSystem.customers.add(c);

    loadCustomerTable();
    generateCustomerId();

    javax.swing.JOptionPane.showMessageDialog(this, "Customer added!");
    }//GEN-LAST:event_btnAddActionPerformed
    
    
    // Update button:
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
            int row = tblCustomers.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Select a customer to update.");
        return;
    }

    Customer c = EcoRideSystem.customers.get(row);

    c.setName(txtName.getText());
    c.setNic(txtNic.getText());
    c.setPhone(txtPhone.getText());
    c.setEmail(txtEmail.getText());
    c.setAddress(txtAddress.getText());

    loadCustomerTable();
    javax.swing.JOptionPane.showMessageDialog(this, "Customer updated!");
    }//GEN-LAST:event_btnUpdateActionPerformed
      // Delete button 
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
            int row = tblCustomers.getSelectedRow();
    if (row == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Select a customer to delete.");
        return;
    }

    EcoRideSystem.customers.remove(row);
     
    // Refresh table and generate next ID
    loadCustomerTable();
    generateCustomerId();

    javax.swing.JOptionPane.showMessageDialog(this, "Customer deleted!");
    }//GEN-LAST:event_btnDeleteActionPerformed

