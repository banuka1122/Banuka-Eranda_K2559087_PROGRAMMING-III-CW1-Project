    /**
     * Loads all customer data from the central EcoRideSystem list into the JTable.
     * This method is critical for displaying the current state of the data model.
     */
    private void loadCustomerTable() {
        // Table column headers
    String[] columns = {"Customer ID", "Name", "NIC", "Phone", "Email", "Address"};
    Object[][] data = new Object[EcoRideSystem.customers.size()][6];


// Loop through the central ArrayList and map object attributes to table rows
    for (int i = 0; i < EcoRideSystem.customers.size(); i++) {
        Customer c = EcoRideSystem.customers.get(i);
        data[i][0] = c.getCustomerId();
        data[i][1] = c.getName();
        data[i][2] = c.getNic();
        data[i][3] = c.getPhone();
        data[i][4] = c.getEmail();
        data[i][5] = c.getAddress();
    }
    
        // Apply the loaded data to the table model
    tblCustomers.setModel(new javax.swing.table.DefaultTableModel(data, columns));
}
