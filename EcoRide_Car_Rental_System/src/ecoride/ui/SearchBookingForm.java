    private void loadFilters() {
    cmbFilter.removeAllItems();
    cmbFilter.addItem("All");
    cmbFilter.addItem("Booking ID");
    cmbFilter.addItem("Customer ID");
    cmbFilter.addItem("Vehicle ID");
    cmbFilter.addItem("Booking Date");
}
   
    private void loadAllResults() {
    String[] cols = {"Booking ID","Customer","Vehicle","Booking Date","Rental Date","Days","KM"};
    Object[][] data = new Object[EcoRideSystem.bookings.size()][7];

            // Fill table row-by-row
    for (int i = 0; i < EcoRideSystem.bookings.size(); i++) {
        Booking b = EcoRideSystem.bookings.get(i);
        data[i][0] = b.getBookingId();
        data[i][1] = b.getCustomer().getCustomerId() + " - " + b.getCustomer().getName();
        data[i][2] = b.getVehicle().getCarId() + " - " + b.getVehicle().getModel();
        data[i][3] = b.getBookingDate().toString();
        data[i][4] = b.getRentalDate().toString();
        data[i][5] = b.getRentalDays();
        data[i][6] = b.getTotalKm();
    }

    tblSearchResults.setModel(new DefaultTableModel(data, cols));
}
    
    private void performSearch() {
    String filter = cmbFilter.getSelectedItem().toString();
    String query = txtSearch.getText().trim().toLowerCase();

    ArrayList<Booking> results = new ArrayList<>();

            // Loop through all bookings and filter based on user selection
    for (Booking b : EcoRideSystem.bookings) {
        switch (filter) {
            case "All":
                results.add(b);
                break;

            case "Booking ID":
                if (b.getBookingId().equalsIgnoreCase(query)) results.add(b);
                break;

            case "Customer ID":
                if (b.getCustomer().getCustomerId().equalsIgnoreCase(query) ||
                    b.getCustomer().getName().toLowerCase().contains(query))
                    results.add(b);
                break;

            case "Vehicle ID":
                if (b.getVehicle().getCarId().equalsIgnoreCase(query) ||
                    b.getVehicle().getModel().toLowerCase().contains(query))
                    results.add(b);
                break;

            case "Booking Date":  
                if (b.getBookingDate().toString().equals(query)) results.add(b);
                break;
        }
    }

    // show results in table
    String[] cols = {"Booking ID","Customer","Vehicle","Booking Date","Rental Date","Days","KM"};
    Object[][] data = new Object[results.size()][7];

    for (int i = 0; i < results.size(); i++) {
        Booking b = results.get(i);
        data[i][0] = b.getBookingId();
        data[i][1] = b.getCustomer().getCustomerId() + " - " + b.getCustomer().getName();
        data[i][2] = b.getVehicle().getCarId() + " - " + b.getVehicle().getModel();
        data[i][3] = b.getBookingDate().toString();
        data[i][4] = b.getRentalDate().toString();
        data[i][5] = b.getRentalDays();
        data[i][6] = b.getTotalKm();
    }

    tblSearchResults.setModel(new DefaultTableModel(data, cols));
}

    
    private void clearTable() {
    tblSearchResults.setModel(new DefaultTableModel(
            new Object[][]{},
            new String[]{"Booking ID","Customer","Vehicle","Booking Date","Rental Date","Days","KM"}
    ));
}
    
