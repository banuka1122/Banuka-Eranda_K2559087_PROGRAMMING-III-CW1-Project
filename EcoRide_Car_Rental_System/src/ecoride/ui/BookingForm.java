    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int row = tblBookings.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Select a booking to update.");
    return;
}

Booking b = EcoRideSystem.bookings.get(row);

// customer can change
String customerId = cmbCustomer.getSelectedItem().toString().split(" - ")[0];
Customer customer = findCustomerById(customerId);
b.setCustomer(customer);

// rental date
LocalDate rentalDate = LocalDate.parse(txtRentalDate.getText());
b.setRentalDate(rentalDate);

// days
int days = Integer.parseInt(txtRentalDays.getText());
b.setRentalDays(days);

loadBookingTable();
JOptionPane.showMessageDialog(this, "Booking updated!");

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblBookings.getSelectedRow();
if (row == -1) {
    JOptionPane.showMessageDialog(this, "Select a booking to delete.");
    return;
}

Booking b = EcoRideSystem.bookings.get(row);

// make vehicle available again
b.getVehicle().setStatus("Available");

EcoRideSystem.bookings.remove(row);

loadBookingTable();
generateBookingId();

JOptionPane.showMessageDialog(this, "Booking deleted!");

    }//GEN-LAST:event_btnDeleteActionPerformed

