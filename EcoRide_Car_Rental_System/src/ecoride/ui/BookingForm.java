    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        
            try {
        String bookingId = lblBookingId.getText();
        LocalDate bookingDate = LocalDate.now();

        String customerSelection = (String) cmbCustomer.getSelectedItem();
        String vehicleSelection = (String) cmbVehicle.getSelectedItem();

        if (customerSelection == null || vehicleSelection == null) {
            JOptionPane.showMessageDialog(this, "Please select customer and vehicle.");
            return;
        }

        Customer customer = findCustomerById(customerSelection.split(" - ")[0]);
        Vehicle vehicle = findVehicleById(vehicleSelection.split(" - ")[0]);

        LocalDate rentalDate = LocalDate.parse(txtRentalDate.getText());
        int rentalDays = Integer.parseInt(txtRentalDays.getText());

        // BUSINESS RULE: Rental must be booked 3 days early
        if (rentalDate.isBefore(bookingDate.plusDays(3))) {
            JOptionPane.showMessageDialog(this, "Booking must be made at least 3 days before rental date.");
            return;
        }

        Booking booking = new Booking(
            bookingId,
            customer,
            vehicle,
            bookingDate,
            rentalDate,
            rentalDays,
            0 // default KM
        );

        EcoRideSystem.bookings.add(booking);

        loadBookingTable();
        generateBookingId();

        JOptionPane.showMessageDialog(this, "Booking added successfully!");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Invalid input. Please check rental date/days.");
    }
        
    }//GEN-LAST:event_btnAddActionPerformed

