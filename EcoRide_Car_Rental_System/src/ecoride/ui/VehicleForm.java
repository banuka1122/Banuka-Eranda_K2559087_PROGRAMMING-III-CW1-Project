    private void loadVehicleTable() {
    String[] columnNames = {"Car ID", "Model", "Category", "Rate", "Status"};
    Object[][] data = new Object[EcoRideSystem.vehicles.size()][5];

    for (int i = 0; i < EcoRideSystem.vehicles.size(); i++) {
        Vehicle v = EcoRideSystem.vehicles.get(i);
        data[i][0] = v.getCarId();
        data[i][1] = v.getModel();
        data[i][2] = v.getCategory();
        data[i][3] = v.getDailyRate();
        data[i][4] = v.getStatus();
    }

    tblVehicles.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));
}
